    inline bool isControlFlow(Mnemonic mnemonic)
    {
        switch (mnemonic)
        {
        case Mnemonic::Call:
        case Mnemonic::Ret:
        case Mnemonic::Iret:
        case Mnemonic::Iretd:
        case Mnemonic::Iretq:
        case Mnemonic::Jmp:
        case Mnemonic::Jb:
        case Mnemonic::Jbe:
        case Mnemonic::Jcxz:
        case Mnemonic::Jecxz:
        case Mnemonic::Jknzd:
        case Mnemonic::Jkzd:
        case Mnemonic::Jl:
        case Mnemonic::Jle:
        case Mnemonic::Jnb:
        case Mnemonic::Jnbe:
        case Mnemonic::Jnl:
        case Mnemonic::Jnle:
        case Mnemonic::Jno:
        case Mnemonic::Jnp:
        case Mnemonic::Jns:
        case Mnemonic::Jnz:
        case Mnemonic::Jo:
        case Mnemonic::Jp:
        case Mnemonic::Jrcxz:
        case Mnemonic::Js:
        case Mnemonic::Jz:
            return true;
        }
        return false;
    }

